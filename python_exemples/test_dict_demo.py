


def test_dict_demo():

    dic = {1:2, 2:3, 3:"super", "c":"Hello world", 4:"end"}
    print(dic) # 1: 2, 2: 3, 3: 'super', 'c': 'Hello world', 4: 'end'
    print(dic[1]) # 2
    print(dic["c"]) # Hello world

    dict = {}
    dict["first_name"] = "alex"
    dict["last_name"] = "niki"
    print(dict) # 'first_name': 'alex', 'last_name': 'niki'